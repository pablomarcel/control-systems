if __package__ in (None, ""):
    # Running as a script from inside state_space_design/observerStatePlotTool.
    pkg_root = os.path.abspath(os.path.join(os.path.dirname(__file__), "..", ".."))
    if pkg_root not in sys.path:
        sys.path.insert(0, pkg_root)

    from state_space_design.observerStatePlotTool.apis import PlotRequest, SimulateOptions
    from state_space_design.observerStatePlotTool.app import ObserverStatePlotApp
else:
    from .apis import PlotRequest, SimulateOptions
    from .app import ObserverStatePlotApp


_PACKAGE = "state_space_design.observerStatePlotTool"
_PROJECT = "modernControl - state_space_design.observerStatePlotTool"
_AUTHOR = "modernControl"
_REQUIRED_MODULES = [
    "state_space_design.observerStatePlotTool.cli",
    "state_space_design.observerStatePlotTool.apis",
    "state_space_design.observerStatePlotTool.app",
]
_OPTIONAL_MODULES = [
    "state_space_design.observerStatePlotTool.core",
    "state_space_design.observerStatePlotTool.io",
    "state_space_design.observerStatePlotTool.plotting",
    "state_space_design.observerStatePlotTool.utils",
]


def _module_exists(module_name: str) -> bool:
    """Return true when Python can locate the module without importing it."""
    try:
        return importlib.util.find_spec(module_name) is not None
    except Exception:
        return False


def _doc_modules() -> list[str]:
    """Return the API modules that are safe for autodoc to import."""
    modules = list(_REQUIRED_MODULES)
    for module_name in _OPTIONAL_MODULES:
        if _module_exists(module_name):
            modules.append(module_name)
    return modules


def _rst_heading(text: str, underline: str = "=") -> str:
    """Return a reStructuredText heading with a safe underline length."""
    return f"{text}\n{underline * len(text)}\n\n"


def _write_if_needed(path: Path, text: str, *, force: bool = False) -> bool:
    """Write text to a file unless it exists and force is false."""
    if path.exists() and not force:
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")
    return True


def _ensure_sphinx_support_dirs(dest: Path) -> None:
    """Create Sphinx support directories with gitkeep files for Git tracking."""
    for dirname in ("_templates", "_static"):
        folder = dest / dirname
        folder.mkdir(parents=True, exist_ok=True)
        gitkeep = folder / ".gitkeep"
        if not gitkeep.exists():
            gitkeep.write_text("", encoding="utf-8")


