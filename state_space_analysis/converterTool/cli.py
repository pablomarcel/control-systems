def build_parser() -> argparse.ArgumentParser:
    """Build the command line parser for transfer-function and state-space conversion."""
    p = argparse.ArgumentParser(description="Convert between transfer-function and state-space models.")

    sub = p.add_subparsers(dest="cmd")

    # Backward-compatible default command: existing flags still work without a subcommand.
    _add_run_arguments(p)

    run = sub.add_parser("run", help="Convert between transfer-function and state-space models.")
    _add_run_arguments(run)

    skel = sub.add_parser("sphinx-skel", help="Create a deploy-safe Sphinx documentation skeleton for this package.")
    skel.add_argument("dest", type=Path, help="Destination docs directory.")
    skel.add_argument("--force", action="store_true", help="Overwrite existing Sphinx skeleton files.")

    return p


def _add_run_arguments(p: argparse.ArgumentParser) -> None:
    """Add converter run arguments to an argparse parser."""
    p.add_argument("--num", type=str, help="Transfer-function numerator coefficients in descending powers.")
    p.add_argument("--den", type=str, help="Transfer-function denominator coefficients in descending powers.")
    p.add_argument("--A", type=str)
    p.add_argument("--B", type=str)
    p.add_argument("--C", type=str)
    p.add_argument("--D", type=str)
    p.add_argument("--iu", type=int, default=0, help="Input channel using zero-based indexing for state-space step plots.")
    p.add_argument("--tfinal", type=float, default=8.0)
    p.add_argument("--dt", type=float, default=1e-3)
    p.add_argument("--no-plot", dest="no_plot", action="store_true", help="Skip plotting.")
    p.add_argument("--sympy", action="store_true", help="Print a SymPy transfer function for SISO cases.")
    p.add_argument("--out-prefix", type=str, help="Prefix for files saved under the package out directory.")
    p.add_argument("--log", type=str, default="INFO", help="Log level: DEBUG, INFO, WARNING, or ERROR.")


def args_to_request(args: argparse.Namespace) -> RunRequest:
    """Convert parsed command line arguments into a RunRequest."""
    return RunRequest(
        num=args.num,
        den=args.den,
        A=args.A,
        B=args.B,
        C=args.C,
        D=args.D,
        iu=args.iu,
        tfinal=args.tfinal,
        dt=args.dt,
        sympy=args.sympy,
        no_plot=args.no_plot,
        out_prefix=args.out_prefix,
        log=args.log,
    )


def _handle_sphinx_skel(args: argparse.Namespace) -> int:
    """Generate Sphinx files and print the created or updated paths."""
    written = _write_sphinx_skeleton(args.dest, force=args.force)
    print(f"Sphinx skeleton ready: {args.dest}")
    if written:
        print("Written files:")
        for path in written:
            print(f"  {path}")
    else:
        print("No existing files were overwritten. Use --force to regenerate.")
    print("Support files ensured:")
    print(f"  {args.dest / '_static' / '.gitkeep'}")
    print(f"  {args.dest / '_templates' / '.gitkeep'}")
    return 0


def main(argv=None) -> None:
    """Run the converter command line interface."""
    parser = build_parser()
    args = parser.parse_args(argv)

    if getattr(args, "cmd", None) == "sphinx-skel":
        raise SystemExit(_handle_sphinx_skel(args))

    app = ConverterApp()
    res = app.run(args_to_request(args))
    raise SystemExit(0 if res.ok else 1)


if __name__ == "__main__":
    main()
