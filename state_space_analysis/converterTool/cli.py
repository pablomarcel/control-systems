from __future__ import annotations

import argparse
import os
import sys
from pathlib import Path

# ---------- Import shim so `python cli.py` works with absolute imports ----------
if __package__ in (None, ""):
    # Running as a script from inside state_space_analysis/converterTool.
    pkg_root = os.path.abspath(os.path.join(os.path.dirname(__file__), "..", ".."))
    if pkg_root not in sys.path:
        sys.path.insert(0, pkg_root)

    from state_space_analysis.converterTool.apis import RunRequest
    from state_space_analysis.converterTool.app import ConverterApp
else:
    from .apis import RunRequest
    from .app import ConverterApp


_PACKAGE = "state_space_analysis.converterTool"
_PROJECT = "modernControl - state_space_analysis.converterTool"
_AUTHOR = "modernControl"
_MODULES = [
    "state_space_analysis.converterTool.cli",
    "state_space_analysis.converterTool.apis",
    "state_space_analysis.converterTool.app",
    "state_space_analysis.converterTool.core",
    "state_space_analysis.converterTool.design",
    "state_space_analysis.converterTool.io",
    "state_space_analysis.converterTool.utils",
]


